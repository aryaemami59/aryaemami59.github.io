function getRelevancy(value, searchTerm, re) {
  if (value === searchTerm) {
    return 4;
  } else if (value.startsWith(searchTerm)) {
    return 3;
  } else if (value.includes(searchTerm)) {
    return 2;
  } else if (value.match(re)) {
    return 1;
  } else {
    return -1;
  }
}
